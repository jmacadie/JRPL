<h2>How does it work?</h2>
<ul>
  <li>Predict the exact score in every match in the 2021 Euros</li>
  <li>Change your prediction as many times as you like before the match</li>
  <li>Matches will be locked down from further prediction
    <strong>30 minutes</strong> before kick-off</li>
  <li>The system will send an e-mail at lock-down saying what every player
    predicted for that match<br />
  <small>Note that the e-mail may not go out exactly half an hour before the
  kick-off but when the first person tries to interact with the website after
  the lock-down point. This ensures that it is not possible to illegally
  tamper with your prediction after the lock-down</small></li>
  <li>You will not know anyone else's predictions before lock-down, which allows
    all players to make predictions without any possibility of gaming a prediction
  relative to someone else. Disclosure of predictions before lock-down is a
  serious offense, so don't do it</li>
  <li>It is essential that you use these 30 minutes between lock-down and kick-off
    to riducule other players' predictions, preferably in public</li>
  <li>After the match is over, and someone has posted the result, the
    predictions will be scored and a further e-mail will be sent out</li>
  <li>In the knock-out stages it is <strong>the score after extra time and
    sudden death (if played)</strong> that counts. Penalty shoot outs are
    ignored from the result and draws can therefore still be predicted</li>
  <li>It costs &pound;20 to participate and this will fund prizes for the best
    predictors</li>
</ul>
<h2>Standard Scoring System</h2>
<p>We're running vanilla JRPL rules this year. There are <u>no muplipliers to
the knock-out stages</u></p>
<p>So points awarded as follows:</p>
<ul>
  <li>
    <p><strong>3 Points</strong> for exactly the right scorline predicted<br />
  <small>e.g. Turkey to beat Italy 2 - 1 and then that's what happens</small></p>
  </li>
  <li>
    <p><strong>1 Point</strong> for the wrong scoreline, but the right result<br />
  <small>e.g. Turkey to beat Italy 2 - 1 but then they win by any other scoreline</small></p>
  </li>
  <li>
    <p><strong>No Points</strong> for the wrong result<br />
  <small>e.g. Turkey to beat Italy 2 - 1 but it's a draw or Italy win</small></p>
  </li>
</ul>
<h2>Auto-Quiz Scoring System</h2>
<p>After much debate (and bemusement) we settled on the following system:</p>
<p><small>Note this scetions is still TBD by the rules committee. Feeling run strong in this area</small></p>
<ul>
  <li>Every prediction is measured for "goodness" by taking its Euclidean
    distance from the result</li>
  <li>Since result is important, as well as home team score and away team score,
    which define a 2D space, the result will mapped on the third dimension. Home
  team win will be worth 0, a draw 3 and away team win 6. Euclidean distance
  will then be measured in 3D space</li>
  <li>Everyone's Euclidean distance (ED) is inverted by subtracting each player's
    ED from the player with the biggest (i.e. worst) ED</li>
  <li>There is a fixed pool of 100 points available for each match</li>
  <li>The fixed pool of points is then allocated to each player proportionate to
    their inverted ED score. This means the player with the worst prediction
    will always get no Auto-Quiz points in the match</li>
  <li>If you miss a prediction you will always get zero Auto-Quiz points</li>
</ul>
