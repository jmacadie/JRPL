<h2>How does it work?</h2>
<ul>
  <li>Predict the exact score in every match in the 2015 Rugby World Cup</li>
  <li>Change your prediction as many times as you like before the match</li>
  <li>Matches will be locked down from further prediction
    <strong>30 minutes</strong> before kick-off</li>
  <li>The system will send an e-mail at lock-down saying what every player
    predicted for that match</li>
  <li>After the match is over, and someone has posted the result, the
    predictions will be scored as follows:<br />
    <br />
    <strong><u>Result</u></strong>
    <ul>
      <li><strong>3 points</strong> for the correct result (e.g. England Win,
        Draw, Fiji Win)</li>
      <li><strong>0 points</strong> if you get the result wrong</li>
    </ul>
    <br />
    <strong><u>Score</u></strong>
    For each of the teams you will be scored on how close your predicted score
    is to the actual score. This happens twice over as you get one set of score
    points for each team in the match. Score points are determined as follows:
    <ul>
      <li><strong>3 points</strong> for the exact score, <i>or</i></li>
      <li><strong>2 points</strong> for predicting a score within 5 of the
        actual score, <i>or</i></li>
      <li><strong>1 points</strong> for predicting a score within 10 of the
        actual score, <i>or</i></li>
      <li><strong>0 points</strong> for predicting a score within 15 of the
        actual score, <i>or</i></li>
      <li><strong>-1 points</strong> for predicting a score within 20 of the
        actual score, <i>or</i></li>
      <li>... and so on losing 1 (score) point for every further 5 you are out
        on the scoreline</li>
    </ul>
    <br />
    <strong><u>Margin</u></strong>
    There are finally points available for the difference between the two team's
    score (or the margin of vicotry). Points work similarly to the score points
    above:
    <ul>
      <li><strong>3 points</strong> for the exact margin (difference between
        teams' scores), <i>or</i></li>
      <li><strong>2 points</strong> for predicting a margin within 5 of the
        actual margin, <i>or</i></li>
      <li><strong>1 points</strong> for predicting a score within 10 of the
        actual margin, <i>or</i></li>
      <li><strong>0 points</strong> for predicting a score within 15 of the
        actual margin, <i>or</i></li>
      <li><strong>-1 points</strong> for predicting a score within 20 of the
        actual margin, <i>or</i></li>
      <li>... and so on losing 1 (margin) point for every further 5 you are out
        on the margin</li>
    </ul>
    <br />
    It is worth noting that score points and margin points can still be gained
    (or lost!) even when the initial result has been predicted wrong. For
    example in a tight match where you predict one side to win by 1 point but in
    fact the other side wins by 1 point you would still get two margin points as
    your prediction of -1 is within 5 points of the actual +1 margin
  </li>
  <li>In addition to the base scores per match set out above multipliers will
    be applied so that matches later in the tournament will earn more points.
    The following multipliers will be applied:
    <ul>
      <li><i>Group Stages</i> - x1 (i.e. no multiplier)</li>
      <li><i>Quarter Finals</i> - x2</li>
      <li><i>Semi Finals</i> - x3</li>
      <li><i>3<sup>rd</sup> 4<sup>th</sup> Place Play-off</i> - x1</li>
      <li><i>Final</i> - x4</li>
    </ul>
  </li>
  <li>In the kncok-out stages it is <strong>the score after extra time and
  suddend death (as played)</strong> that counts. The kicking competition is
  ignored from the result and draws can therefore still be predicted</li>
</ul>
